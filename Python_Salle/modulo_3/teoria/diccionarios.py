# Los diccionarios son estructuras de datos que almacenan pares clave-valor. Son útiles para almacenar información relacionada y acceder a ella de manera eficiente.
# Un diccionario se define utilizando llaves `{}` y los pares clave-valor se separan por comas. Las claves deben ser únicas y pueden ser de cualquier tipo inmutable (como cadenas, números o tuplas), mientras que los valores pueden ser de cualquier tipo.
# Ejemplo de un diccionario simple:
persona = {
    "nombre": "Juan",
    "edad": 30,
    "ciudad": "Madrid"
}
# Acceder a los valores del diccionario utilizando las claves:
print(persona["nombre"])  # Imprime: Juan
print(persona["edad"])    # Imprime: 30